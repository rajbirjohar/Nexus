const Tiptap = (props) => {
  const { setFieldValue, name, oldContent } = props

  const editor = useEditor({
    extensions: [
      StarterKit,
      CharacterCount.configure({ limit }),
      Underline,
      Placeholder.configure({
        // Use a placeholder:
        placeholder: 'What do you want to share with the whole wide world today?',
        emptyEditorClass: `${styles.isEmpty}`,
        // Use different placeholders depending on the node type:
        // placeholder: ({ node }) => {
        //   if (node.type.name === 'heading') {
        //     return 'Whatâ€™s the title?'
        //   }

        //   return 'Can you add some further context?'
        // },
      }),
    ],
    content: oldContent || '',
  })
  // Cannot update a component while rendering another component without a useEffect
  useEffect(() => {
    // editor will be null for a split second on render
    if (editor) {
      // every time a change happens
      editor.on('update', ({ editor }) => {
        // set the new field value so we don't have to double click in order for
        // isSubmitting prop to record the new value
        setFieldValue(name, editor.getHTML())
      })
    }
    // Supress warning for setFieldValue and name
    /* eslint-disable */
  }, [editor])

  if (!editor) {
    return null
  }

  return (
    <div>
      <MenuBar editor={editor} />
      <div className={styles.editor}>
        <EditorContent editor={editor} name={name} />
      </div>
      <span className={styles.maxlength}>
        {editor.storage.characterCount.characters()}/{limit}
      </span>
    </div>
  )
}

export default Tiptap
